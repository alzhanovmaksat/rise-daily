// Helper function to get random affirmation for time of day
export const getRandomAffirmation = (timeOfDay) => {
  const affirmationsList = affirmations[timeOfDay];
  const randomIndex = Math.floor(Math.random() * affirmationsList.length);
  return affirmationsList[randomIndex];
};

// Helper function to determine current time of day
export const getCurrentTimeOfDay = () => {
  const hour = new Date().getHours();
  
  if (hour >= 6 && hour < 12) {
    return 'morning';
  } else if (hour >= 12 && hour < 18) {
    return 'afternoon';
  } else {
    return 'evening';
  }
};
